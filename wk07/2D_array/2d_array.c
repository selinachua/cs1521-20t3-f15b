#include <stdio.h>

char flag[6][12] = {
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'}
};

// flag[0][0] -> 0 * 12 + 0 = 0
// flag[2][3] -> 2 * 12 + 3 = 27

// row * N_COL + col
int main(void) {
    for (int row = 0; row < 6; row++) {
        for (int col = 0; col < 12; col++) {
            char ch = flag[row][col];
            printf ("%c", ch);
        }
        printf ("\n");
    }
    return 0;
}